package webdata.utils;

/**
 * a Mutable pair of integers
 */
public class IntPair {
    public int first;
    public int second;

    /**
     * @param first  the first int
     * @param second the second int
     */
    public IntPair(int first, int second) {
        this.first = first;
        this.second = second;
    }
}
